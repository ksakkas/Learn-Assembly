;C to Assembly
;A[10] = b + A[6]
;b = $s2
;Η διεύθυνση βάσης του A είναι στον $s3
;ΠΡΟΣΟΧΗ: offset(μετατόπιση) = 4 byte ανά λέξη

lw $t0, 24($s3) ;t0 = s3 + 24(s3) => t0 = A[6]
add $t1, $s2, $t0 ;t1 = s2 + t0
sw 40($s3), $t1 ;40(s3) = t0 => A[10] = t1